c      if(cphigeigmode) then
      ificg=421
      open(ificg,file='CphiGeig',form='unformatted')
c      else   
c        allocate(
c     &  cphi_d(ldim2, nband, nqbze,nsp),
c     &  geig_d(ngpmx, nband, nqbze,nsp)
