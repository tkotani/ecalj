      end

      subroutine strip(str,i1,i2)
C!  strip(str,i1,i2) -- Returns i1,i2 so that str(i1:i2) stripped.
      character*(*) str
      i1=0
      do 1 i=1,len(str)
        if(str(i:i).ne.' ') then
          i1=i
          goto 2
        endif
  1   continue
      i1=1
      i2=0
      return
  2   i2=len(str)+1
      do 3 i=len(str),1,-1
        if(str(i:i).ne.' ') then
          i2=i
          goto 4
        endif
  3   continue
  4   continue
      end

      subroutine words(str,nw)
C- Count blank-delimited words in str
C ----------------------------------------------------------------------
Ci Inputs
Ci   str   :string
Co Outputs
Co   nw    :number of blank-delimited words in str
Cu Updates
C ----------------------------------------------------------------------
C     implicit none
C ... Passed parameters
      character*(*) str
      integer nw
C ... Local parameters
      integer i1,i2,i0,i

      nw = 0
      i1 = 0
      i2 = 0
      i0 = 1
   99 do  10  i = i0, len(str)
        if(str(i:i) .ne. ' ') then
          i1 = i
          goto 90
        endif
