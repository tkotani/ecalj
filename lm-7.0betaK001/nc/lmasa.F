        rsrnge = dgets('ham rsrnge',sham)
        ifi = fopna('sigm',-1,4)
        call rdsigm(1000+lrsigl,nbas,nsp,ldham(1),slat,sham,
     .    sbz,ifi,rsrnge,lwsig)
        call fclose(ifi)
        sxad = T
      endif

