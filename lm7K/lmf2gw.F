     &        rydberg*(vvv3(ibx)-vvv2(ibx))
            enddo
          endif
 888      continue
        enddo
      enddo
      print *,' rrr: xxxxxxxxxxxxxxxxxxxx'
c      if(cphigeigmode) 
      close(ificg)
      deallocate( cphi,evl, vvv1,vvv2,vvv3)


