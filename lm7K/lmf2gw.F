      real(8),allocatable :: wt(:),q0i(:,:)
c----------------------------------------------
      integer ::
     &nsp,      ! =1 or 2, corresponding to para or ferro.
     &nbas,     ! Number of atom in the primitive cell
     &nclass,   ! Number of atomic class (or type) for in the primitive cell
     &nrmx,     ! = maxval(nr(1:nclass))  Maximum number of nr
     &ncoremx,  ! = maxval(ncore(1:nclass))
c
     &lmxamx,   ! = maxval(lmxa(1:nclass))
     &ngpmx,    ! Maximum number of G vector.
     &nband,    ! Number of bands given by GWIN0
     &ldim,     ! = sum ( (lmxa(1:nbas)+1)**2 )
     &ldim2,    ! = total number of augmentation functions nRlm
     &nphi,     ! = total number of augmentation functions nRl
     &nqbze     ! = nqbz*(1+nq0i). Number of q points given by qg4gw
      real(8) ::
     &alat,    ! Lattice constant in a.u.
     &efermi = 1d99   ! This is dummy now .
      integer,allocatable::
     &iclass(:),
     &lmxa_d  (:),
     &nr(:),
     &konf_d(:,:),
     &ncore_d(:)
      real(8),allocatable ::
     &zz(:),
     &aa(:),bb(:),
     &ec_d (:,:,:),
     &evl_d(:,:,:),
     &vxclda (:,:,:),
     &gx_d(:,:,:,:,:),
     &gcore_d(:,:,:,:)
      complex(8),allocatable::
     &geig_d(:,:,:,:)
      integer,allocatable:: nindx(:),lindx(:),ibasindx(:)

c      logical :: cphigeigmode=.true.
      integer:: ificg
c-----------------------------------------------------------------------

c ------------------------------------------
c dimension work space
      integer,parameter :: nsize= 1 000 000
Cdwdef1       real w(nsize)
Cdwdef1       common /w/ w
c ------------------------------------------

cccccccccccccccccccccccccccccccc

      print *, 'extension=?'
      ext0 = ' '
      read(*,'(a8)') ext0
      call strip(ext0,i1,i2)
      ext = '.' // ext0(i1:i2)
      ifi = fext(ext)

c- OPEN *.gwb
C      ifigwa = 331
C      ifigwb = 332
C      ifigwx1 =334
C      ifigwx2 =335
C      call mfdef(100)
      ifichkv = 8485
      call setpr(30)
      ifigwa = fopna('gwa',-1,4)
      ifigwb = fopna('gwb',-1,4)
      ifigwx1 = fopna('gw1',-1,4)
      ifigwx2 = fopna('gw2',-1,4)

C      call mopen(ifigwa,'gwa','u')
C      call mopen(ifigwb,'gwb','u')
C      call mopen(ifigwx1,'gw1','u')
C      call mopen(ifigwx2,'gw2','u')
c---
      open(ifichkv,file='VXCFP.chk')
      write(ifichkv,"(a)") '### LDA exchange correlation ###'
      write(ifichkv,"(a)")
     &'#   qvec                 ikp iband    eigen
     &VXC(ntotal)     VXC(nvalence)'
c---
c for simplicity.
      nocore=.false.
c$$$      inquire(file='NoCore',exist=nocore)
c$$$      if(nocore) then
c$$$          write(6,"(a)")
c$$$     &  ' FIND NoCore. So we caluculate Vxc(nvalence)
c$$$     &   from *.gw3 given by ng0 with mode 3.'
c$$$        ifigwx3 =336
c$$$          print *,' If you can not find *.gw3. Do echo 3|ng0.'
c$$$C       call mopen(ifigwx3,'gw3','u')
c$$$        ifigwx3 = fopna('gw3',-1,4)
c$$$      endif
c---
      read (ifigwb) nbas,nsp,ldim2,ndimh,alat,qlat,ef0,
c     &              n1q,n2q,n3q,plat(1:3,1:3)
     &nqbz,plat(1:3,1:3),nqbze
      allocate(lmxaa(1:nbas), bas(1:3,1:nbas))
      read (ifigwb) lmxaa(1:nbas), bas(1:3,1:nbas)
      read (ifigwb) ngpmx, ngcmx

C      No longer needed
C      ifi = fopnx('Q0P',2,1,-1)
C      read (ifi,*) nq0i
C      allocate( wt(nq0i),q0i(1:3,nq0i) )
C      read (ifi,*) ( wt(i),q0i(1:3,i),i=1,nq0i)
C      call fclose(ifi)
C      write(6,'('' File Q0P:'',i2,'' special gamma-point(s)'')') nq0i

c --- takao --- readin GWIN0.
!      ifigw0  = fopnx('GWIN0',2,1,-1)
!      read(ifigw0,*)
!      read(ifigw0,*)
!      read(ifigw0,*)
!      read(ifigw0,*)  QpGcut_phi, QpGcut_Cou  ! cut off for G. |k+G|< QpGcut in  (a.u.)
!      read(ifigw0,*)
!      read(ifigw0,*)
!      read(ifigw0,*)
!c---takao June 2003
!c     read(ifigw0,*) nband
!      call readin000(ifigw0,nband1,emax1)
!      call readin000(ifigw0,nband2,emax2)
!      nband = max(nband1,nband2)
!      close(ifigw0)
!      if(nband>ndimh) nband = ndimh
!      write(6,'('' File GWIN0: nband  ='',i4)') nband
c
      lmxamx = maxval(lmxaa(1:nbas))
c      nqbz  = n1q*n2q*n3q
c      nqbze = nqbz*(1 + nq0i) !This is readin now. Jan2004 takao
c
      allocate( iclass(nbas) )

c--readin CLASS
      ifefclass = 321
      open(ifefclass,file='CLASS')
c     read(ifefclass,*) efermi
      nclass=0
      do ibas = 1,nbas
        read(ifefclass,*)  ibasx,  nclassx
c        print *,'class ',ibasx,  nclassx
        if(nclassx>nclass) nclass= nclassx
      enddo
c
      write(6,'('' File CLASS:  nclass ='',i4)') nclass

      allocate(
     &qbze(3,nqbze),
     &lmxa_d   (nclass),
     &nr     (nclass),
     &ncore_d(nclass) ,
     &konf_d(0:lmxamx,nclass),
     &zz(nclass),
     &aa(nclass),  bb(nclass)
     &)
      write(6,"(' allocation 1 end' )") 

! cphigeigmode=T stores cphi and geig into CphiGeig.
! This procedure can omit memory allocation of cphi and geig.
