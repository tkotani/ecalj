      call suldau(nbasp,sspec,ssite,nlibu,lmaxu,w(olldau))
      call pack5('ham nlibu lmaxu',sham,nlibu,lmaxu,0,0,0)
        
C ... Read LDA+U Hamiltonian
      if (nlibu .gt. 0) then
        call rlse(ovorb)
        i = nsp*nlibu*(lmaxu*2+1)**2
        call defcc(ovorb,-i)
        call defcc(odmatu,-i)
        call defcc(odmato,-i)
