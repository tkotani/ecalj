      integer function bitand(a,b)
C- Returns the bitwise AND of two integers
C     implicit none
      integer a,b
      integer TF(64),i,n,i1,i2
      if (a .lt. 0) then
        bitand = b
      elseif (b .lt. 0) then
        bitand = a
      else
        i1 = a
        i2 = b
        n = 0
        do  10  i = 1, 64
          if (i1 .eq. 0 .or. i2 .eq. 0) goto 12
          TF(i) = (mod(i1,2)+mod(i2,2))/2
          i1 = i1/2
          i2 = i2/2
          n = n+1
   10   continue
   12   continue
        bitand = 0
        do  20  i = n, 1, -1
   20   bitand = bitand*2 + tf(i)
      endif
      end

