        dmxeu(4) = 0
        dmxeu(5) = 0
        dmxeu(34) = 1
        call pqmix(nclasp,nl,w(olmx),nsp,i,nit,outs,dmxeu,
     .    0,w(opnu),w(oqnu),w(oxold),w(owk),w(owk2),w(oxnew))
        call pvpqm1(1,sspec,nclass,nclasp,nsp,nbas,0,F,F,T,neul,w(oics),
