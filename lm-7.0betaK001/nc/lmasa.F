      if (mod(lsx,2) .eq. 1 .or. mod(iscr,2) .eq. 1)
     .  call pack1('ctrl lasa',sctrl,lasa-bitand(lasa,4))
      call bndasa(sctrl,slat,sspec,ssite,sarray,sbz,sham,spot,sstrn,
     .  w(ovorb),w(odmatu),
     .  efermi,w(oeband),nband,nevmx,w(oqnu),sumev,w(orhos),amag,
